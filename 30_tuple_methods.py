# кортеж - это упорядоченная, неизменяемая коллекция произвольных данных
point = 1, 2
print(point)
# присваивание кортежа из одного элемента
point = (1, )
point = 1,
print(point)
# кортежное присваивание
x, y = 1, 2
print(x, y)
x, y = ['hello', 'world']
print(x, y)
# len() - длина кортежа
x = 1, 2, 3
print(len(x))
